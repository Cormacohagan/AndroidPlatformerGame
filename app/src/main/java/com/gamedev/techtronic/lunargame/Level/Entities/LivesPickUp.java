package com.gamedev.techtronic.lunargame.Level.Entities;


import android.graphics.Bitmap;

import com.gamedev.techtronic.lunargame.gage.world.GameScreen;

public class LivesPickUp extends PickUp {

    private float livesIncreaseValue;

    public LivesPickUp(float x, float y, float width, float height, Bitmap bitmap,
                        GameScreen gameScreen, String pickUpType1, float livesIncreaseValue) {
        super(x, y, width, height, bitmap, gameScreen, pickUpType1);
        this.livesIncreaseValue = livesIncreaseValue;
    }

    public void setLivesIncreaseValue(float livesIncreaseValue) {
        this.livesIncreaseValue = livesIncreaseValue;
    }

    public float getLivesIncreaseValue() {
        return livesIncreaseValue;
    }
}
